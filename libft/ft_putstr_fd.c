#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	x;

	if (fd < 0 || !s)
		return ;
	x = 0;
	while (s[x])
	{
		write (fd, &s[x], 1);
		x++;
	}
}
